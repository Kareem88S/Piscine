#include "bsq.h"

void	print_map(t_map *map)
{
	int	i;
	int	j;

	j = 0;
	while (j < map->height)
	{
		i = 0;
		while (i < map->width)
		{
			write(1, &map->map[j][i], 1);
			i++;
		}
		write (1, "\n", 1);
		j++;
	}
}
